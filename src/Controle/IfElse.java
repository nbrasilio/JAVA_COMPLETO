package Controle;

public class IfElse {
	
	public static void main(String[] args) {
		
		double nota = 7.5;
		
		if (nota >= 7.0) 
		{
			System.out.println("Aprovado!");
			System.out.println("Parabéns");
		} 
		else 
		{
			System.out.println("Recuperação!");
			System.out.println("Persista e estude!");
		}
		
		
	}

}
